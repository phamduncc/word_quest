  /// Get words for a specific category.
  static List<WordEntry> getWords(WordCategory category) {
    return words[category] ?? [];
  }

  /// Get all words across all categories.
  static List<WordEntry> getAllWords() {
    return words.values.expand((list) => list).toList();
  }

  /// Get words for a category, selecting a subset based on count and seed.
  static List<WordEntry> getWordsForPuzzle(
    WordCategory category,
    int count,
    int seed,
  ) {
    final categoryWords = List<WordEntry>.from(getWords(category));
    // Use seed for deterministic shuffling
    final random = _seededShuffle(categoryWords, seed);
    return random.take(count.clamp(1, categoryWords.length)).toList();
  }

  /// Deterministic shuffle using a simple seed.
  static List<WordEntry> _seededShuffle(List<WordEntry> list, int seed) {
    final result = List<WordEntry>.from(list);
    var s = seed;
    for (var i = result.length - 1; i > 0; i--) {
      s = (s * 1103515245 + 12345) & 0x7fffffff;
      final j = s % (i + 1);
      final temp = result[i];
      result[i] = result[j];
      result[j] = temp;
    }
    return result;
  }
